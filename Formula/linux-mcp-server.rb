  def install
    # Set environment for cryptography build to find openssl
    ENV["OPENSSL_DIR"] = Formula["openssl@3"].opt_prefix
    ENV["OPENSSL_LIB_DIR"] = Formula["openssl@3"].opt_lib
    ENV["OPENSSL_INCLUDE_DIR"] = Formula["openssl@3"].opt_include
    # Use CMake builder for aws-lc-sys to avoid jitterentropy -O0 conflict with Homebrew's -Os
    ENV["AWS_LC_SYS_CMAKE_BUILDER"] = "1"
    # Ensure PyO3/maturin-based extensions can link on macOS (resolve Python symbols at load time)
    ENV["CARGO_ENCODED_RUSTFLAGS"] = "-Clink-arg=-undefined\x1f-Clink-arg=dynamic_lookup" if OS.mac?

    venv = virtualenv_create(libexec, "python3.12")

    # Install hf-xet separately: aws-lc-sys's jitterentropy requires -O0 but
    # Homebrew sets -Os globally which overrides CMake's per-target flags
    ENV.O0 { venv.pip_install resource("hf-xet") }

    venv.pip_install resources.reject { |r| r.name == "hf-xet" }
    venv.pip_install_and_link buildpath

    # Install the goose configuration setup script
    (bin/"goose-mcp-setup").write <<~BASH
      #!/bin/bash
      set -e

      CONFIG_DIR="${HOME}/.config/goose"
      CONFIG_FILE="${CONFIG_DIR}/config.yaml"
      MCP_SERVER="#{HOMEBREW_PREFIX}/bin/linux-mcp-server"
      USERNAME="$(whoami)"

      if [ -f "$CONFIG_FILE" ]; then
        echo "Config already exists at $CONFIG_FILE"
        echo ""
        echo "To add linux-mcp-server manually, add this to your extensions section:"
        echo ""
        echo "  linux-tools:"
        echo "    enabled: true"
        echo "    type: stdio"
        echo "    name: linux-tools"
        echo "    description: Linux system administration and diagnostics"
        echo "    cmd: ${MCP_SERVER}"
        echo "    envs:"
        echo "      LINUX_MCP_USER: ${USERNAME}"
        echo "      LINUX_MCP_LOG_LEVEL: INFO"
        echo "    timeout: 30"
        echo "    bundled: null"
        echo "    available_tools: []"
        echo "    args: []"
        exit 0
      fi

      mkdir -p "$CONFIG_DIR"

      {
        echo "GEMINI_CLI_COMMAND: gemini"
        echo "GOOSE_PROVIDER: gemini-cli"
        echo "GOOSE_MODEL: gemini-3-flash-preview"
        echo "extensions:"
        echo "  linux-tools:"
        echo "    enabled: true"
        echo "    type: stdio"
        echo "    name: linux-tools"
        echo "    description: Linux system administration and diagnostics"
        echo "    cmd: ${MCP_SERVER}"
        echo "    envs:"
        echo "      LINUX_MCP_USER: ${USERNAME}"
        echo "      LINUX_MCP_LOG_LEVEL: INFO"
        echo "      LINUX_MCP_SSH_KEY_PATH: ~/.ssh/id_ed25519"
        echo "    timeout: 30"
        echo "    bundled: null"
        echo "    available_tools: []"
        echo "    args: []"
      } > "$CONFIG_FILE"

      echo "Created goose config at $CONFIG_FILE"
      echo "You can now use goose with linux-mcp-server!"
    BASH
  end

